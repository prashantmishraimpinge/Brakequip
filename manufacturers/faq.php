    <h4>In a nutshell - How does it work?</h4>
    <p>You purchase a combined crimp/test machine with a start-up stock kit of hose and fittings. We also support you with training aides, catalogues, marketing and technical information. BrakeQuip has at hand large stocks of hose and fittings available so there is no need to carry a large inventory/stock.</p>

    <h4>I don't have any experience making hoses!</h4>
    <p>Don't worry; this system has been made as fool proof as possible. There is nothing to adjust, everything is pre-set and the machine is very simple to operate. A comprehensive operations manual comes standard with the system. Click here to see just how easy it is to make a brake hose! We are always just a phone call away.</p>

    <h4>What hoses can I make?</h4>
    <p>The BrakeQuip system allows you to make any sort of brake hose from any sort of vehicle, including go-karts, motorbikes, trucks, forklifts and industrial machinery.

    <h4>How do I stock all the fittings in the market place?</h4>
    <p>You will never need to carry every fitting. We select the fitting range that covers 85-90% of your market place. To keep your inventory cost low, the fittings are designed to be universal where possible. The rest of the range is achieved by reusing the customers fitting. This is a very simple procedure, and you will require the 'old' hoses.</p>

    <h4>What about liability?</h4>
    <p>Every hose is pressure tested to 3000psi. This pressure test procedure is a mandatory part of the BrakeQuip manufacturing system. The test pressure is twice the maximum operating pressure of any given vehicle.<br />
    There is also a 'sales agreement' that accompanies every machine and outlines the obligations of both parties.</p>

    <h4>Do the hoses comply with legal safety standards?</h4>
    <p>YES! All brake hoses that are manufactured with the BrakeQuip system comply with Australian & International standards... ADR42/04 FMVSS106.</p>

    <h4>How long does it take to make and test a brake hose?</h4>
    <p>If the fittings are in stock it takes between two and five minutes. If you are to reuse the old fittings, it can take between 15 and 20 minutes.</p>

